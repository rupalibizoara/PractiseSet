package com.rupali.util;



@Entity
public class Elevator {
	
	
	@A
	private int A;
	private String B;
	private String C;
	private String D;
	public int getA() {
		return A;
	}
	public void setA(int a) {
		A = a;
	}
	public String getB() {
		return B;
	}
	public void setB(String b) {
		B = b;
	}
	public String getC() {
		return C;
	}
	public void setC(String c) {
		C = c;
	}
	public String getD() {
		return D;
	}
	public void setD(String d) {
		D = d;
	}  
	
	


}
